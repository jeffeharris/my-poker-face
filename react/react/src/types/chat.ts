export interface ChatMessage {
  id: string;
  sender: string;
  message: string;
  timestamp: string;
  type: 'player' | 'ai' | 'table' | 'system';
  action?: string; // Optional action text (e.g., "raised to $50") for AI messages
  phase?: string; // Optional game phase for card-deal messages (e.g., "flop")
  cards?: string[]; // Optional card strings for card-deal messages (e.g., ["A♠", "K♦"])
  win_result?: WinResult; // Optional structured winning hand data
}

/** Structured data for winning hand display in chat */
export interface WinResult {
  winners: string;
  pot: number;
  is_showdown: boolean;
  hand_name?: string; // Only for showdown
  winner_cards?: string[]; // Hole cards, e.g. ["A♠", "K♠"]
  community_cards?: string[]; // Board, e.g. ["Q♠", "J♠", "10♠", "5♥", "2♦"]
  winning_combo?: string[]; // Best 5-card combo rank names, e.g. ["A", "K", "Q", "J", "10"]
}

/**
 * Backend message format (before transformation to ChatMessage).
 * Uses the same message_type values as ChatMessage.type.
 */
export interface BackendChatMessage {
  id?: string;
  sender: string;
  content: string;
  timestamp: string;
  message_type: ChatMessage['type'];
  action?: string; // Optional action text for AI messages
  phase?: string; // Optional game phase for card-deal messages
  cards?: string[]; // Optional card strings for card-deal messages
  win_result?: WinResult; // Optional structured winning hand data
}

/**
 * Psychological manipulation goals for targeted chat suggestions.
 *
 * The live mid-hand palette is the six trait-keyed intents below
 * (`intimidate`/`dare`/`trash_talk`/`props`/`flatter`/`befriend`), each
 * keyed off a *different* recipient attribute so the set is a toolkit. Two
 * of them — `intimidate` (poise) and `dare` (ego, inverted) — are
 * **emotional-layer** tones: they move the target's composure/confidence
 * (and thus their play) via `react_to_social_stimulus`, not the
 * relationship axes. See docs/plans/SOCIAL_TEMPERAMENT_AND_QUICKCHATS.md §5.
 *
 * `tilt`/`bait`/`needle`/`goad` are the retired hostile near-duplicates
 * (all collapsed into `trash_talk`); kept in the union for back-compat with
 * stored prefs / in-flight requests but no longer shown. `bluff` is a
 * no-target-effect tone (talk about your own hand).
 */
export type ChatTone =
  | 'intimidate'
  | 'dare'
  | 'trash_talk'
  | 'props'
  | 'flatter'
  | 'befriend'
  | 'bluff'
  // Retired hostile near-duplicates (folded into `trash_talk`):
  | 'tilt'
  | 'bait'
  | 'needle'
  | 'goad';

/**
 * Length modifier for chat suggestions.
 */
export type ChatLength = 'short' | 'long';

/**
 * Delivery register for chat suggestions. `sarcastic` is a third
 * mutually-exclusive position (not an intensity scalar): it means "don't
 * read the surface literally," and its effect flips with the tone —
 * sharpening a warm tone into a barb, softening a hostile one into banter,
 * or turning a self-directed one into dry self-mockery. Only offered on
 * tones with a surface to invert (the warm/hostile relationship tones), not
 * the emotional-layer ones.
 */
export type ChatIntensity = 'chill' | 'spicy' | 'sarcastic';

/**
 * A targeted chat suggestion generated by the AI.
 */
export interface TargetedSuggestion {
  text: string;
  tone: ChatTone;
}

/**
 * Response from the targeted-chat-suggestions endpoint.
 */
export interface TargetedSuggestionsResponse {
  suggestions: TargetedSuggestion[];
  targetPlayer: string | null;
  fallback?: boolean;
  error?: string;
}

/**
 * Post-round chat tones based on win/loss outcome.
 * After a WIN:  gloat (poise→tilt), gracious, humble, commiserate (to a bystander)
 * After a LOSS: salty, props, cry_luck (ego poke), vow (poise rattle)
 * `props` is shared (tip-of-the-cap respect — available win or lose).
 *
 * `gloat`/`cry_luck`/`vow` are emotional-layer tones (move the target's
 * composure/confidence). See docs/plans/SOCIAL_TEMPERAMENT_AND_QUICKCHATS.md §5.
 */
export type PostRoundTone =
  | 'gloat'
  | 'humble'
  | 'salty'
  | 'gracious'
  | 'props'
  | 'commiserate'
  | 'cry_luck'
  | 'vow';

/**
 * A post-round chat suggestion.
 */
export interface PostRoundSuggestion {
  text: string;
  tone: PostRoundTone;
}

/**
 * Response from the post-round-chat-suggestions endpoint.
 */
export interface PostRoundSuggestionsResponse {
  suggestions: PostRoundSuggestion[];
  fallback?: boolean;
  error?: string;
}
